# El parámetro end, se utiliza para agregar cualquier otra
# cadena de caracteres al final de la salida e impresión
# en pantalla de la función print().

# Además, por defecto la función print() genera un salto
# de línea al terminar su ejecución, sin embargo, con
# ayuda del parámetro end, es posible evitar este salto de línea.

#Ejemplo para el parametro end

print("Esto es un", end=" -*/") # El parámetro "end" se usa básicamente para una impresión
print("ejemplo")                # en pantalla personalizada o en algún momento interactiva,
                                # y nos junta el texto con el print que tiene justo
                                # de bajo de del

# Ejemplo para el parametro sep

print("1","2","3","4","5", sep="-") # El parámetro "sep" nos ayuda a controlar el espacio
                                    # entre los elementos dentro del print









