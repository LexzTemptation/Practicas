#Función len()

# En Python, la función len() nos
# permite obtener la longitud de una
# cadena de caracteres, o bien, el
# número de elementos que componen a
# un objeto.

#Opción 1
print("Hola tiene", len("Hola"), "caracteres.")

#Opción 2
longitud = len("Lexz Temptation")
print("Lexz Temptation", longitud, "caracteres")

